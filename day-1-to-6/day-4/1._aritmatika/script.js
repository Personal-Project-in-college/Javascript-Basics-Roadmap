    // TODO : Aritmatika digunakan untuk melakukan operasi matematika seperti penjumlahan dan pengurangan

    console.log(10 - 3); // * Output 7
    console.log(2 + 4); // * Output 6

    // 1. Penambahan
    let Penambahan = 10 + 15
    console.log(Penambahan) // * Output 25
        // Bisa untuk tipe data string
        let message = "Hello " + "human!";
        console.log(message); // * Output Hello human!
        
        // Namun ketika tipe datanya berbeda akan dilakukan perubahan paksa ke tipe data string
        let sum = "Hi " + 89;
        console.log(sum); // * Output Hi 89

    // 2. Pengurangan
    let pengurangan = 20 - 5
    console.log(pengurangan) // * Output 15
    
    // 3. Perkalian 
    let perkalian = 7 * 8
    console.log(perkalian) // * Output 56

    // 4. Perpangkatan ( Sisi kiri di pangkatkan oleh sisi kanan )
    let pangkatkan = 3 ** 3
    console.log(pangkatkan) // * Output 27

    // 5. Pembagian
    let pembagian = 8 / 2
    console.log(pembagian) // * Output 4
    
    // 6. Modulus (Sisa Bagi)
    let modulus = 100 % 9
    console.log(modulus) // * Output 1

    // 7. Increment
    let increment = 10
    increment++  // +1
    increment++  // +1
    console.log(increment) // * Output 12
    
    // 8. Decrement
    let decrement = 10
    decrement--
    decrement--
    console.log(decrement) // * Output 8

// ! Operator logika digunakan untuk memeriksa apakah satu atau lebih ekspresi menghasilkan True atau False. Operator ini hanya dapat mengembalikan nilai Boolean

    // ?   Logical AND (&&) (Mengembalikan true jika semua kondisi true, jika tidak, mengembalikan false)
    console.log(true && false); // * Output false

    // ?   Logical OR (||) (Mengembalikan true jika salah satu kondisi true, jika tidak, mengembalikan false)
    console.log(false || false); // * Output false

    // ?   Logical NOT (!) (Membalikkan hasilnya: mengembalikan true jika false dan sebaliknya)
    console.log(!true); // * Output false